#ifndef COLORS_H
#define COLORS_H

// Reset
#define RESET "\033[0m"

// Regular Colors
#define BLACK "\033[0;30m"        // Black
#define RED "\033[0;31m"          // Red
#define GREEN "\033[0;32m"        // Green
#define YELLOW "\033[0;33m"       // Yellow
#define BLUE "\033[0;34m"         // Blue
#define MAGENTA "\033[0;35m"      // Magenta
#define CYAN "\033[0;36m"         // Cyan
#define WHITE "\033[0;37m"        // White

// Background Colors
#define BG_BLACK "\033[40m"       // Background Black
#define BG_RED "\033[41m"         // Background Red
#define BG_GREEN "\033[42m"       // Background Green
#define BG_YELLOW "\033[43m"      // Background Yellow
#define BG_BLUE "\033[44m"        // Background Blue
#define BG_MAGENTA "\033[45m"     // Background Magenta
#define BG_CYAN "\033[46m"        // Background Cyan
#define BG_WHITE "\033[47m"       // Background White

// Bold High Intensity
#define BOLD_INTENSE_BLACK "\033[1;90m"   // Bold Intense Black
#define BOLD_INTENSE_RED "\033[1;91m"     // Bold Intense Red
#define BOLD_INTENSE_GREEN "\033[1;92m"   // Bold Intense Green
#define BOLD_INTENSE_YELLOW "\033[1;93m"  // Bold Intense Yellow
#define BOLD_INTENSE_BLUE "\033[1;94m"    // Bold Intense Blue
#define BOLD_INTENSE_MAGENTA "\033[1;95m" // Bold Intense Magenta
#define BOLD_INTENSE_CYAN "\033[1;96m"    // Bold Intense Cyan
#define BOLD_INTENSE_WHITE "\033[1;97m"   // Bold Intense White

#endif // COLORS_H
